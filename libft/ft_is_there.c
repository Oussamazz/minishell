#include "libft.h"

int		ft_is_there(char *s, char c)
{
	int i;

	if (s)
	{
		i = 0;
		while (s[i])
		{
			if (s[i] == c)
				return (1);
			i++;
		}
		if (c == '\0' && s[i] == '\0')
			return (1);
		return (0);
	}
	return (0);
}
